          <img src="assets/image/gedung.jpg" alt="sejarah"  class="featured-image"/>
          <p>
               Menurut bahasa Tidore kata Papo Ua artinya "tidak bergabung", "tidak bersatu",
               atau "tidak bergandengan". Maksudnya wilayah Papua itu jauh sehingga tidak masuk
               dalam daerah induk Kesultanan Tidore walau wilayah-wilayah tersebut tetap tunduk 
               dan berada dibawah persekutuan dagang Tidore bernama Uli Siwa. Dalam pembagiannya
               wilayah di Papua dibagi menjadi Korano Ngaruha atau Kepulauan Raja Ampat, 
               Papo Ua Gamsio (Papua sembilan negeri), dan Mafor Soa Raha (Mafor Empat Soa).
               [9] Teori lain nama Papua berasal dari bahasa Melayu papuwah, artinya "rambut keriting".
               Akan tetapi kata ini baru masuk pada kamus bahasa melayu tahun 1812 ciptaan William Marsden
               yang tidak ditemukan dalam kamus yang lebih awal,[10] sedangkan catatan abad ke-16 
               Portugis dan Spanyol, kata Papua sudah dipakai dan hanya merujuk kepada penduduk 
               Kepulauan Raja Ampat dan Semenanjung Kepala Burung.[11] Berdasarkan teori lain ini
                menurut F.C. Kamma nama ini bisa saja berasal dari Bahasa Biak Sup i Babwa yang digunakan 
                untuk menyebut Kepulauan Raja Ampat berarti tanah di-bawah (matahari terbenam), 
                yang kemudian menjadi Papwa lalu Papua.
          </p>
 
          <p>
               Masyarakat di bagian barat Papua mengenal filosofi “Satu Tungku Tiga Batu”. Inilah filosofi 
               yang menjaga kerukunan umat beragama di sana.Tungku yang merupakan alat masak diketahui punya
               bobot berat dan bersuhu sangat panas. Di bawahnya, ada batu-batu yang disusun sedemikian rupa 
               agar bisa menopang tungku tersebut. Tungku dan batu dengan posisi demikianlah yang dijadikan 
               simbol persatuan masyarakat Papua Barat, khususnya Fakfak. Tiga batu sendiri menggambarkan 
               tiga agama besar di Papua Barat, yakni Katolik, Islam, dan Kristen. Ketiganya harus terus bersatu
               dan tidak boleh ada yang terpisah agar tungku yang ditopang di atasnya tidak jatuh dan tumpah.
          </p>
