    <h2>Geografis</h2>
    <img src="assets/image/papua.jpg" alt="geografis"  class="featured-image"/>
    <p>
           Provinsi Papua Barat merupakan Provinsi yang memiliki cerita panjang sebelum menjadi bagian wilayah Indonesia. 
           Didalam peta, provinsi ini terletak pada bagian kepala burung Pulau Papua dengan beberapa pulau-pulau disekitarnya.
           Secara geografis  Provinsi Papua Barat terletak antara 00– 40 Lintang Selatan dan antara 1240– 1320 Bujur Timur
           dengan Luas wilayah Papua Barat adalah berupa daratan seluas 102.955,15 km2. Provinsi Papua Barat kaya akan 
           sumberdaya alam dan bentangan pesona alam yang indah. Diantaranya Kepulauan Raja Ampat dengan empat gugusan
           pulau meliputi Pulau Waigeo, Pulau Misool, Pulau Salawati dan Pulau Bantata, kepulauan ini merupakan salah
           satu dari 10 perairan terbaik untuk diving.
      </p>
 
      <p>
           Taman Nasional Burung Cendrawasi, yang merupakan taman nasional 
           perairan laut terluas di Indonesia serta Taman Nasional Lorens yang merupakan Taman Nasional terluas di Asia Tenggara.
           Keadaan suhu rata-rata di Provinsi Papua berkisar antara 27,20 C hingga 27,90C dengan curah hujan yang cukup tinggi
           sepanjang tahun, tertinggi pada bulan Maret dan Desember.
      </p>
 
